public class AddWholeNum {
    public static void main(String[] args) { //activity 4 AddWholeNum

        int sum = 0;
        for (int i = 50; i <= 100; i++) {
            sum = sum +i;
        }
        System.out.println("sum of first 50 numbers is" + sum);
    }
}
